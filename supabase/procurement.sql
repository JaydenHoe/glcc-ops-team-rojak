-- ============================================================
-- CONSTRUCTION PROCUREMENT — Phase 1 schema + sample data.
-- Paste this whole block into the Supabase SQL Editor (your project) and Run once.
-- Safe to re-run: it DROPS and recreates ONLY the 11 procurement tables and
-- reseeds them. It does NOT touch `records` / `bot_memory` (the original starter).
-- Sample data is Singapore/Malaysia plumbing & sanitary construction procurement.
-- ============================================================

drop table if exists procurement_tasks         cascade;
drop table if exists invoices                   cascade;
drop table if exists deliveries                 cascade;
drop table if exists purchase_order_items       cascade;
drop table if exists purchase_orders            cascade;
drop table if exists rfqs                       cascade;
drop table if exists purchase_requisition_items cascade;
drop table if exists purchase_requisitions      cascade;
drop table if exists materials                  cascade;
drop table if exists suppliers                  cascade;
drop table if exists projects                   cascade;

-- 1) Construction projects / sites --------------------------------------------
create table projects (
  id            bigint generated by default as identity primary key,
  name          text not null,
  site_location text,
  client_name   text,
  budget_amount numeric(14,2) not null default 0,
  start_date    date,
  end_date      date,
  status        text not null default 'active',   -- active | on_hold | completed
  created_at    timestamptz not null default now()
);

-- 2) Suppliers / vendors ------------------------------------------------------
create table suppliers (
  id             bigint generated by default as identity primary key,
  company_name   text not null,
  trade_category text,                              -- Pipes & Fittings | Sanitary Ware | Pumps & Valves ...
  contact_person text,
  email          text,
  phone          text,
  payment_terms  text,                              -- e.g. '30 days', 'COD'
  rating         numeric(2,1) default 0,            -- 0.0 - 5.0
  status         text not null default 'active',    -- active | inactive | blacklisted
  created_at     timestamptz not null default now()
);

-- 3) Material catalog ---------------------------------------------------------
create table materials (
  id                  bigint generated by default as identity primary key,
  name                text not null,
  category            text,                          -- Pipes | Sanitary | Pumps | Valves | Traps | Fittings
  unit                text,                          -- length | pcs | set | metre | unit
  estimated_unit_cost numeric(12,2) default 0,
  preferred_supplier_id bigint references suppliers(id) on delete set null,
  created_at          timestamptz not null default now()
);

-- 4) Purchase Requisitions (PR) ----------------------------------------------
create table purchase_requisitions (
  id              bigint generated by default as identity primary key,
  pr_number       text not null,
  project_id      bigint references projects(id) on delete set null,
  requested_by    text,                              -- site engineer / requester name
  required_date   date,
  status          text not null default 'draft',     -- draft | submitted | approved | rejected
  priority        text not null default 'normal',    -- low | normal | high | urgent
  description     text,
  estimated_total numeric(14,2) default 0,
  created_at      timestamptz not null default now()
);

create table purchase_requisition_items (
  id                  bigint generated by default as identity primary key,
  pr_id               bigint references purchase_requisitions(id) on delete cascade,
  material_id         bigint references materials(id) on delete set null,
  description         text,
  quantity            numeric(12,2) default 0,
  unit                text,
  estimated_unit_cost numeric(12,2) default 0,
  estimated_total     numeric(14,2) default 0
);

-- 5) RFQ / supplier quotes ----------------------------------------------------
create table rfqs (
  id            bigint generated by default as identity primary key,
  rfq_number    text not null,
  pr_id         bigint references purchase_requisitions(id) on delete set null,
  supplier_id   bigint references suppliers(id) on delete set null,
  status        text not null default 'sent',        -- sent | quote_received | declined | closed
  sent_date     date,
  due_date      date,
  quoted_amount numeric(14,2),
  created_at    timestamptz not null default now()
);

-- 6) Purchase Orders (PO) -----------------------------------------------------
create table purchase_orders (
  id                bigint generated by default as identity primary key,
  po_number         text not null,
  project_id        bigint references projects(id) on delete set null,
  supplier_id       bigint references suppliers(id) on delete set null,
  pr_id             bigint references purchase_requisitions(id) on delete set null,
  po_date           date,
  delivery_due_date date,
  status            text not null default 'approved', -- approved | partially_delivered | delivered | invoiced | paid
  approval_status   text not null default 'pending',  -- pending | approved | rejected
  approved_by       text,
  total_amount      numeric(14,2) default 0,
  created_at        timestamptz not null default now()
);

create table purchase_order_items (
  id          bigint generated by default as identity primary key,
  po_id       bigint references purchase_orders(id) on delete cascade,
  material_id bigint references materials(id) on delete set null,
  description text,
  quantity    numeric(12,2) default 0,
  unit        text,
  unit_price  numeric(12,2) default 0,
  line_total  numeric(14,2) default 0
);

-- 7) Deliveries / goods receipt ----------------------------------------------
create table deliveries (
  id                    bigint generated by default as identity primary key,
  po_id                 bigint references purchase_orders(id) on delete cascade,
  delivery_order_number text,
  received_date         date,
  status                text not null default 'received', -- received | partial | rejected
  received_by           text,
  remarks               text,
  created_at            timestamptz not null default now()
);

-- 8) Invoices -----------------------------------------------------------------
create table invoices (
  id             bigint generated by default as identity primary key,
  invoice_number text not null,
  po_id          bigint references purchase_orders(id) on delete set null,
  supplier_id    bigint references suppliers(id) on delete set null,
  invoice_date   date,
  due_date       date,
  amount         numeric(14,2) default 0,
  status         text not null default 'pending',   -- pending | paid | overdue
  paid_date      date,
  created_at     timestamptz not null default now()
);

-- 9) Procurement tasks (approvals & actions) ---------------------------------
create table procurement_tasks (
  id           bigint generated by default as identity primary key,
  title        text not null,
  description  text,
  related_type text,                                  -- pr | po | rfq | delivery | invoice
  related_id   bigint,
  assigned_to  text,                                  -- role / person
  due_date     date,
  priority     text not null default 'normal',        -- low | normal | high | urgent
  status       text not null default 'open',          -- open | in_progress | done
  created_at   timestamptz not null default now()
);

-- Server-only access (the app reads with the service_role key).
alter table projects                    enable row level security;
alter table suppliers                   enable row level security;
alter table materials                   enable row level security;
alter table purchase_requisitions       enable row level security;
alter table purchase_requisition_items  enable row level security;
alter table rfqs                        enable row level security;
alter table purchase_orders             enable row level security;
alter table purchase_order_items        enable row level security;
alter table deliveries                  enable row level security;
alter table invoices                    enable row level security;
alter table procurement_tasks           enable row level security;

